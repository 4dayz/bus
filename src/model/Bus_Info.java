package model;

public class Bus_Info {
	private String bus_num;
	private String grd_code;
	private String grd_name;
	private String all_seat;
	private String b_company;
	private String start_time;
	
	public String getBus_num() {return bus_num;	}
	public void setBus_num(String bus_num) {this.bus_num = bus_num;	}
	public String getGrd_code() {return grd_code;	}
	public void setGrd_code(String grd_code) {this.grd_code = grd_code;	}
	public String getGrd_name() {return grd_name;	}
	public void setGrd_name(String grd_name) {	this.grd_name = grd_name;	}
	public String getAll_seat() {return all_seat;}
	public void setAll_seat(String all_seat) {this.all_seat = all_seat;	}
	public String getB_company() {return b_company;	}
	public void setB_company(String b_company) {this.b_company = b_company;	}
	public String getStart_time() {	return start_time;}
	public void setStart_time(String start_time) {this.start_time = start_time;	}
	
}
